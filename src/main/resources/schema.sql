create database DEV_CRUD_APP_DATABASE;

CREATE TABLE DEV_CRUD_APP_DATABASE.CRUD_APP_USER
(
    ID_USER INT(9) auto_increment primary key NOT NULL,
    USERNAME_USER VARCHAR(255) NOT NULL,
    PASSWORD_USER VARCHAR(255) NOT NULL,
    LOGIN_COUNT_USER INT(9) NOT NULL
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE DEV_CRUD_APP_DATABASE.CRUD_APP_PERSON
(
    ID_PERSON INT(9) auto_increment PRIMARY KEY NOT NULL,
    ID_USER INT(9),
    NAME_PERSON VARCHAR(255) NOT NULL,
    AGE_PERSON INT(9) NOT NULL,
    BIRTHDAY_PERSON VARCHAR(255) NOT NULL,
    foreign key (ID_USER)
        references CRUD_APP_USER(ID_USER)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE DEV_CRUD_APP_DATABASE.CRUD_APP_ROLE
(
    ID_ROLE INT(9) auto_increment PRIMARY KEY NOT NULL,
    NAME_ROLE VARCHAR(255) NOT NULL,
    CODE_ROLE VARCHAR(255) NOT NULL,
    DESCRIPTION_ROLE VARCHAR(255) NOT NULL
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE DEV_CRUD_APP_DATABASE.CRUD_APP_REL_USER_ROLE
(
    ID_REL_USER_ROLE INT(9) auto_increment PRIMARY KEY NOT NULL,
    ID_USER INT(9) NOT NULL,
    ID_ROLE INT(9) NOT NULL,
    foreign key (ID_USER)
        references CRUD_APP_USER(ID_USER),
    foreign key (ID_ROLE)
        references CRUD_APP_ROLE(ID_ROLE)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;